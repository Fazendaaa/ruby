							Verhoeff algorithm

	The  Verhoeff  algorithm is a checksum formula for error detection developed
	by the Dutch mathematician Jacobus Verhoeff and was first published in 1969.
	It   was   the  first  decimal  check  digit  algorithm  which  detects  all
	single-digit  errors,  and  all  transposition errors involving two adjacent
	digits, which was at the time thought impossible with such a code.	

